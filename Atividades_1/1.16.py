# Faça um programa que leia quatro números inteiros, calcule e mostre a soma desses números.

a = int(input("Informe o primeiro número inteiro : "))
b = int(input("Informe o segundo número inteiro : "))
c = int(input("Informe o terceiro número inteiro : "))
d = int(input("Informe o quarto número inteiro : "))

soma = (a + b +c + d)

print("A somatória de {} + {} + {} + {} é = {}".format(a,b,c,d,soma))